package udemy.data.structure.algorithms.linkedlist;

public class Node{
	String value;
	Node next;
	
	Node(String value){
		this.value = value;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public Node getNext() {
		return next;
	}

	public void setNext(Node next) {
		this.next = next;
	}

}
